import math

print(round(1.3))  # redondea el número al mas cercano
print(abs(-77))  # valor absoluto
print(abs(55))  # valor absoluto

print(math.ceil(1.1))  # llevarlo al número superior mas cercano
print(math.floor(1.9999))  # llevarlo al numero inferior más cercano
print(math.isnan(23))  # valida si corresponde a un numero
print(math.pow(10, 3))  # potencia 3 de 10
print(math.sqrt(9))  # raiz cuadrada
